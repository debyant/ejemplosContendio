package ejemplosContendio;

public class ejemploSumaVariable {

	public static void main(String[] args) {
		int numero1 = 5; //asignación de valor
		int numero2 = 9; //asignación de valor
		System.out.println("valor declarado: " + numero2);
		numero2 = numero1; //le asignamos un nuevo valor
		System.out.println("nuevo valor asignado: " + numero2);

	}
}
